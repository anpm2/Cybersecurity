def rol4(n, l):
    return ((n << l) | (n >> (32 - l))) & 0xFFFFFFFF

key = b"neko_hat"
flag = ''
cipher_dword = []

for i in range(0, len(cipher), 4):
    j = (cipher[i + 3] << 24) | (cipher[i + 2] << 16) | (cipher[i + 1] << 8) | cipher[i]
    j &= 0xFFFFFFFF
    cipher_dword.append(j)

for i in range(len(cipher_dword)):
    val = cipher_dword[i]
    val -= 30000
    val -= key[i % 4]
    val ^= key[i % 8]
    val = rol4(val, 4)
    flag += chr(val)

print(f'Flag: {flag}')
